#include <unistd.h>

void	inter(char *s1, char *s2)
{
	char	arr[256] = {0};	// заполняем массив нулями
	int		i;

	i = 0;
	while (s2[i])
	{
		arr[(int)s2[i]] = 1;	// переводим по ASCII символ второй строки в число и ставим 1 в массиве
		i++;
	}
	i = 0;
	while (s1[i])
	{
		if (arr[(int)s1[i]] == 1)	// если символ второй строки == символу первой, то в массиве уже будет 1
		{
			write(1, &s1[i], 1);	// печатаем совпавший символ
			arr[(int)s1[i]] = 2;	// и больше не повторяемся - ставим в массиве 2
		}
		i++;
	}
}

int	main(int argc, char	**argv)
{
	if (argc == 3)	// защита - если на вход подаётся не 2 строки, то печатем '\n'
		inter(argv[1], argv[2]);	// подаём в функцию две строки
	write(1, "\n", 1);
	return (0);
}
